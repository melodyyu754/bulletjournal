package cs3500.pa05.controller;

/**
 * Represents a type of action that may be taken from the menu bar.
 */
public enum Action {
  /**
   * Action to open the file
   */
  OPEN_FILE,
  /**
   * Action to save the file
   */
  SAVE_FILE,
  /**
   * Action to create a new event
   */
  EVENT,
  /**
   * Action to create a new task
   */
  TASK,
  /**
   * Action to create a new week
   */
  WEEK,
  /**
   * Action to open the settings dialog
   */
  SETTINGS,
  /**
   * Actions to change the theme
   */
  THEME }
